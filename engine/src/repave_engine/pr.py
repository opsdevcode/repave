from __future__ import annotations

from dataclasses import dataclass
from pathlib import Path

from repave_engine.target_repo import ModuleRepository


@dataclass(frozen=True)
class PullRequestPlan:
    title: str
    body: str
    branch: str
    files_root: Path
    repository: ModuleRepository


def plan_pull_request(
    *,
    module_name: str,
    blueprint_name: str,
    blueprint_version: str,
    standard_version: str,
    files_root: Path,
    repository: ModuleRepository,
) -> PullRequestPlan:
    branch = "main"
    title = (
        f"Bootstrap Terraform module {module_name} (repave {blueprint_name}@{blueprint_version})"
    )
    body = (
        "## Summary\n"
        f"Generated by repave blueprint `{blueprint_name}` v{blueprint_version}.\n\n"
        f"- Standard version: `{standard_version}`\n"
        f"- Target repository: `{repository.web_url}`\n"
        "- Output type: governed module repository\n"
        "- Gates: enforced before publish\n\n"
        "## Rollback\n"
        "Do not merge or delete the repository if this scaffold should be discarded.\n"
    )
    return PullRequestPlan(
        title=title,
        body=body,
        branch=branch,
        files_root=files_root,
        repository=repository,
    )


def create_pull_request(plan: PullRequestPlan, *, github_token: str | None) -> str:
    if not github_token:
        return (
            "Dry-run: remote GitHub PR not created. Provide GITHUB_TOKEN to enable "
            "governed remote output.\n"
            f"Target repository: {plan.repository.web_url}\n"
            f"Local repository: {plan.repository.local_path}\n"
            f"Planned branch: {plan.branch}\n"
            f"Planned title: {plan.title}"
        )

    # v1.0 intentionally stops at local repo bootstrap; GitHub remote/PR lands in v1.1+.
    return (
        "GitHub repository creation and PR output are not yet wired in v1.0. "
        "The module was materialized in its own local git repository.\n"
        f"Target repository: {plan.repository.web_url}\n"
        f"Local repository: {plan.repository.local_path}\n"
        f"Planned title: {plan.title}"
    )
