from __future__ import annotations

from dataclasses import dataclass
from pathlib import Path


@dataclass(frozen=True)
class PullRequestPlan:
    title: str
    body: str
    branch: str
    files_root: Path


def plan_pull_request(
    *,
    module_name: str,
    blueprint_name: str,
    blueprint_version: str,
    standard_version: str,
    files_root: Path,
) -> PullRequestPlan:
    branch = f"repave/{blueprint_name}/{module_name}"
    title = f"Add Terraform module {module_name} (repave {blueprint_name}@{blueprint_version})"
    body = (
        "## Summary\n"
        f"Generated by repave blueprint `{blueprint_name}` v{blueprint_version}.\n\n"
        f"- Standard version: `{standard_version}`\n"
        "- Output type: governed pull request\n"
        "- Gates: enforced before merge\n\n"
        "## Rollback\n"
        "Close this PR without merging.\n"
    )
    return PullRequestPlan(title=title, body=body, branch=branch, files_root=files_root)


def create_pull_request(plan: PullRequestPlan, *, github_token: str | None) -> str:
    if not github_token:
        return (
            "Dry-run: PR not created. Provide GITHUB_TOKEN to enable governed PR output.\n"
            f"Planned branch: {plan.branch}\n"
            f"Planned title: {plan.title}\n"
            f"Files root: {plan.files_root}"
        )

    # v1.0 intentionally stops at planning; GitHub App/OIDC integration lands in v1.1+.
    return (
        "GitHub PR creation is not yet wired in v1.0. "
        "Rendered files and gate results are available locally.\n"
        f"Planned branch: {plan.branch}\n"
        f"Planned title: {plan.title}"
    )
